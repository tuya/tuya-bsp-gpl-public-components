static int bmi088_probe(struct i2c_client *client,
                         const struct i2c_device_id *id)
{
	dev_info(&client->dev, "%s enter\n", __func__);

	if (!i2c_check_functionality(client->adapter, I2C_FUNC_I2C)) {
		dev_err(&client->dev, "bmi088 check_functionality failed\n");
		return -ENODEV;
	}

	bmi088 = devm_kzalloc(&client->dev, sizeof(*bmi088), GFP_KERNEL);
	if (!bmi088)
		return -ENOMEM;

	bmi088->delay = 1000;
	bmi088->enSensors = BMI088_ACCGYR_ENABLE;
	bmi088->client = client;
	atomic_set(&bmi088->open_flag, 0);
	i2c_set_clientdata(client, bmi088);

	INIT_DELAYED_WORK(&bmi088->work, bmi088_input_func);

	/* Initialize the bmi088 sensor */
	if (bmi088_init() != 0) {
		dev_err(&client->dev, "bmi088 init failed\n");
		return -1;
	}

	/* Declare input device */
	bmi088->input_dev = devm_input_allocate_device(&client->dev);
	if (!bmi088->input_dev) {
		dev_err(&client->dev, "bmi088 allocate input failed\n");
		return -ENOMEM;
	}
	/* Setup input device */
	set_bit(EV_ABS, bmi088->input_dev->evbit);

	input_set_abs_params(bmi088->input_dev, ABS_X, ABSMIN_ACC, ABSMAX_ACC, 0, 0);
	input_set_abs_params(bmi088->input_dev, ABS_Y, ABSMIN_ACC, ABSMAX_ACC, 0, 0);
	input_set_abs_params(bmi088->input_dev, ABS_Z, ABSMIN_ACC, ABSMAX_ACC, 0, 0);

	input_set_abs_params(bmi088->input_dev, ABS_RX, ABSMIN_GYRO, ABSMAX_GYRO, 0, 0);
	input_set_abs_params(bmi088->input_dev, ABS_RY, ABSMIN_GYRO, ABSMAX_GYRO, 0, 0);
	input_set_abs_params(bmi088->input_dev, ABS_RZ, ABSMIN_GYRO, ABSMAX_GYRO, 0, 0);

	bmi088->input_dev->name = "bmi088";

	/* Register */
	if (input_register_device(bmi088->input_dev)) {
		dev_err(&client->dev, "bmi088 register input failed\n");
		return -ENODEV;
	}

	/* Register as a misc device */
	misc_register(&bmi088_device);

	return 0;
}

static int bmi088_remove(struct i2c_client *client)
{
	dev_info(&client->dev, "%s enter\n", __func__);

	misc_deregister(&bmi088_device);
	input_unregister_device(bmi088->input_dev);
	cancel_delayed_work(&bmi088->work);

    return 0;
}

