/*!
 *  @brief This API writes the config stream data in memory using burst mode.
 */
static int stream_transfer_write(const u8 *stream_data, u16 index)
{
	int rslt;
	u8 asic_msb = (uint8_t)((index / 2) >> 4);
	u8 asic_lsb = ((index / 2) & 0x0F);

	/* Write to feature config register */
	rslt = bmi088_i2c_write(bmi088a_addr, BMI08X_REG_ACCEL_RESERVED_5B, &asic_lsb, 1);

	/* Write to feature config register */
	rslt |= bmi088_i2c_write(bmi088a_addr, BMI08X_REG_ACCEL_RESERVED_5C, &asic_msb, 1);

	/* Write to feature config registers */
	rslt |= bmi088_i2c_write(bmi088a_addr, BMI08X_REG_ACCEL_FEATURE_CFG, (u8 *)stream_data, 32);

	return rslt;
}

