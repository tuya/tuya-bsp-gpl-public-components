/**
* @brief Check bmi088 chip id
* @return 0:success  other:error
*/
static int bmi088_check_id(void)
{
	int rslt;
	u8 chip_id[2] = {0};

	rslt = bmi088_i2c_read(bmi088a_addr, BMI08X_REG_ACCEL_CHIP_ID, &chip_id[0], 1);
	delay_ms(1);
	rslt |= bmi088_i2c_read(bmi088g_addr, BMI08X_REG_GYRO_CHIP_ID, &chip_id[1], 1);
	delay_ms(1);
	if ((rslt == BMI08X_OK) &&
		(chip_id[0] == BMI088_ACCEL_CHIP_ID) &&
		(chip_id[1] == BMI08X_GYRO_CHIP_ID)) {
		printk("bmi088 chip id:0x%x 0x%x\n", chip_id[0], chip_id[1]);
		return BMI08X_OK;
	}

	return BMI08X_E_DEV_NOT_FOUND;
}

/**
* @brief bmi088 device soft reset
* @return none
*/
