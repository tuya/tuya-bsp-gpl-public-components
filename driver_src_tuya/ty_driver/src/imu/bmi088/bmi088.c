static int bmi088_i2c_read(u16 addr, u8 reg, u8 *buf, u8 len)
{
	u8 i = 0;
	struct i2c_client *client = bmi088->client;

	struct i2c_msg msg[2] = {
		[0] = {
			.addr  = addr,
			.flags = 0,
			.len   = 1,
			.buf   = &reg,
		},
		[1] = {
			.addr  = addr,
			.flags = I2C_M_RD,
			.len   = len,
			.buf   = buf,
		},
	};

	while (i++ < I2C_RETRY_CNT) {
		if (2 == i2c_transfer(client->adapter, msg, 2))
			break;

		mdelay(10);
	}

	if (i == I2C_RETRY_CNT+1) {
		dev_err(&client->dev, "bmi088 read rigster error!\n");
		return BMI08X_E_COM_FAIL;
	}

	return BMI08X_OK;
}

static int bmi088_i2c_write(u16 addr, u8 reg, u8 *buf, u8 len)
{
	u8 i = 0;
	struct i2c_msg msg;
	u8 data[MAX_DATA_SIZE] = {0};
	struct i2c_client *client = bmi088->client;

	data[0] = reg;
	memcpy(&data[1], buf, len);

	msg.addr  = addr;
	msg.flags = 0;
	msg.len   = len + 1;
	msg.buf   = data;

	while (i++ < I2C_RETRY_CNT) {
		if (1 == i2c_transfer(client->adapter, &msg, 1))
			break;

		mdelay(10);
	}

	if (i == I2C_RETRY_CNT+1) {
		dev_err(&client->dev, "bmi088 write rigster error!\n");
		return BMI08X_E_COM_FAIL;
	}

	return BMI08X_OK;
}

static void delay_ms(unsigned short mSecond)
{
	mdelay(mSecond);
}


